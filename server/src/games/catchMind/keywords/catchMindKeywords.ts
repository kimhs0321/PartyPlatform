export function getCatchMindWordsByCategoryKeys(categoryKeys: readonly string[]) {
  const selectedKeys = new Set(categoryKeys);
  return CATCH_MIND_CATEGORIES
    .filter((category) => selectedKeys.has(category.key))
    .flatMap((category) => [...category.words]);
}

export function getCatchMindWordsByDifficulty(
  difficulties: readonly CatchMindDifficulty[],
) {
  const selectedDifficulties = new Set(difficulties);
  return CATCH_MIND_CATEGORIES
    .filter((category) => selectedDifficulties.has(category.difficulty))
    .flatMap((category) => [...category.words]);
}
